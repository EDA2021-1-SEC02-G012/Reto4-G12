def loadLandingPoints(analyzer, filename):
    """.DS_Store"""
    landingFile = cf.data_dir + filename
    input_file = csv.DictReader(open(landingFile, encoding="utf-8"),
                                delimiter=",")
    
    for i in input_file:
        mp.put(analyzer['landing_points'], i['landing_point_id'], i)

